const i18nResources = Object.fromEntries(
  Object.entries(UI_TRANSLATIONS).map(([langKey, translationDict]) => [
    langKey,
    { translation: translationDict }
  ])
);

i18n
  .use(initReactI18next)
  .init({
    resources: i18nResources,
    lng: "en",
    fallbackLng: "en",
    interpolation: {
      escapeValue: false
    }
  });

export function t(key: string, lang = "en"): string {
  if (i18n.isInitialized) {
    // Standardize to matching code mapping
    const validCodes = ["en", "sw", "es", "fr", "de", "ar"];
    const activeLng = validCodes.includes(lang) ? lang : "en";
    return i18n.t(key, { lng: activeLng });
  }

  // Fallback before initialization finishes
  const codeMap: Record<string, keyof typeof UI_TRANSLATIONS> = {
    en: "en",
    sw: "sw",
    es: "es",
    fr: "fr",
    de: "de",
    ar: "ar"
  };
  const targetCode = codeMap[lang] || "en";
  const dict = UI_TRANSLATIONS[targetCode] || UI_TRANSLATIONS["en"];
  return dict[key as keyof typeof dict] || UI_TRANSLATIONS["en"][key as keyof typeof dict] || key;
}

export default i18n;
